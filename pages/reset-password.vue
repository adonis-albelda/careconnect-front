<template>
  <div class="login-mobile-wrapper">
    <div class="login-cont">
      <div class="auth-back" @click="goTo('index')">
        <span>
          <i class="icon-arrow-left"></i>
          Back
        </span>
      </div>
      <h2>Create new password</h2>
      <p>
        Your new password must be different from your previous used passwords.
      </p>
      <ValidationObserver v-slot="{ handleSubmit, reset }">
        <form
          @submit.prevent="handleSubmit(handleLogin)"
          @reset.prevent="reset"
        >
          <div class="login-form">
            <ValidationProvider
              v-slot="{ errors }"
              name="Password"
              rules="required"
            >
              <div class="input-cont">
                <label>Password</label>
                <div :class="['for-cont', errors[0] ? 'error-msgg' : '']">
                  <input
                    class="text-box"
                    v-model="user.password"
                    type="password"
                    placeholder="Password"
                  />
                  <small>Must be at least 8 characters.</small>
                  <span>{{ errors[0] }}</span>
                </div>
              </div>
            </ValidationProvider>
            <ValidationProvider
              v-slot="{ errors }"
              name="Password Confirmation"
              rules="required"
            >
              <div class="input-cont">
                <label>Confirm Password</label>
                <div :class="['for-cont', errors[0] ? 'error-msgg' : '']">
                  <input
                    class="text-box"
                    v-model="user.password"
                    type="password"
                    placeholder="Confirm Password"
                  />
                  <small>Both passwords must match.</small>
                  <span>{{ errors[0] }}</span>
                </div>
              </div>
            </ValidationProvider>
            <button class="login-btn">reset password</button>
            <button class="login-btn cancel" @click.prevent="goTo('index')">Cancel</button>
          </div>
        </form>
      </ValidationObserver>
      <div class="login-copyright">
        <p>Copyright Â© 2022 Care Connect - All Rights Reserved.</p>
      </div>
    </div>
  </div>
</template>
<script>
export default {
  layout: 'TwoColumnLayout',
  auth: 'guest',
  head: {
    bodyAttrs: {
      id: 'login-page',
    },
  },
  data() {
    return {
      user: {
        email: '',
        password: '',
      },
      isRequesting: false,
    }
  },
  async created() {
    const token = this.$route.query.code
    if (!token) this.goTo('index')

    let isCodeValid = await this.isCodeValid(token)
    if (!isCodeValid) this.goTo('index')
  },  
  methods: {
    async isCodeValid(code) {
      try {
        const {data} = await this.$axios.post('code/status', {code: code}) 
        return true
      } catch(e) {
        return false
      }
    },
    async handleLogin() {
      try {
        if (this.isRequesting) return
        this.isRequesting = true

        const res = await this.$auth.loginWith('local', {
          data: {
            email: this.user.email,
            password: this.user.password,
          },
        })

        console.log(res, 'res')

        if (res.data) {
          this.showSuccess('Succesfully logged in !')
        }

        setTimeout(() => {
          this.isRequesting = false
          this.goTo('index')
        }, 5000)
      } catch (e) {
        if (e.response.status === 404) {
          this.showError('Invalid credentials, please try again!')
        } else {
          this.showError('Something went wrong processing your request!')
        }
        this.isRequesting = false
      }
    },
  },
}
</script>
